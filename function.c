void Led_init(void)
{
    /* I/O port C clock enabled */
    RCC->APB2ENR |= RCC_APB2ENR_IOPCEN;
    /* Output mode, max speed 50 MHz */
    GPIOC->CRH |= GPIO_CRH_MODE13;
    /* General purpose output push-pull */
    GPIOC->CRH &= ~(GPIO_CRH_CNF13);
}

