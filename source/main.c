/*******************************************************************************
 * Include
 ******************************************************************************/
#include "stm32f10x.h"
#include "function.h"

/*******************************************************************************
 * Define
 ******************************************************************************/
#define DELAY 5000000
/*******************************************************************************
 * Function
 ******************************************************************************/
void Delay(uint32_t number);

/*******************************************************************************
 * Code
 ******************************************************************************/
int main(void)
{
    Led_init();
    while(1)
    {
        GPIOC->ODR &= ~(GPIO_ODR_ODR13);
        Delay(DELAY);
        GPIOC->ODR |= GPIO_ODR_ODR13;
        Delay(DELAY);
    }
}

void Delay(uint32_t number)
{
    while(number > 0)
    {
        number--;
    }
}
